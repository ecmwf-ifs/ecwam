      INTEGER(KIND=JWIM), INTENT(IN) :: NFIELDS, NGPTOTG, NCA, NRA
      REAL(KIND=JWRB),    INTENT(IN) :: FIELDS(NGPTOTG,NFIELDS)
      LOGICAL,            INTENT(IN) :: LWCUR
      INTEGER(KIND=JWIM), INTENT(INOUT) :: MASK_IN(NGPTOTG)
