      IF (.NOT.ASSOCIATED(OUTWSPEC_IO_SERV_HANDLER)) CALL WAM_ABORT('OUTWSPEC_IO_SERV_HANDLER IS NOT INITIALIZED')
      CALL OUTWSPEC_IO_SERV_HANDLER (IJS, IJL, SPEC, MARSTYPE, CDATE, IFCST)

      END SUBROUTINE OUTWSPEC_IO_SERV

