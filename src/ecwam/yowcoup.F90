      CALL IFSTOWAM_HANDLER (BLK2LOC,                    &
           &                 NFIELDS, NGPTOTG, NCA, NRA, &
           &                 FIELDS, LWCUR, MASK_IN,     &
           &                 NXS, NXE, NYS, NYE, FIELDG)

      END SUBROUTINE IFSTOWAM

